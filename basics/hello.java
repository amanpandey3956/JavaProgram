class hello {
    public static void main(String[] args) {

        System.out.println("hello world");

        // Arithmetic operator 

        int a = 5;
        int b = 4;
        System.out.println("a+b = " + (a + b));
        System.out.println("a-b = " + (a - b));
        System.out.println("a*b = " + (a * b));
        System.out.println("a/b = " + (a / b));

        //logical operator

        System.out.println(a > b && a == b);
        System.out.println(a < b && a == 10);

        // Comparisonal operator

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);

    }
}
